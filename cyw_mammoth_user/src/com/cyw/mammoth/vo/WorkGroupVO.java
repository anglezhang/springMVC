package com.cyw.mammoth.vo;

import java.io.Serializable;

import com.cyw.mammoth.bean.WorkGroup;
/**
 * 工作组VO
 * @author Administrator
 *
 */
public class WorkGroupVO implements Serializable {
	
	private static final long serialVersionUID = 8719072379602492082L;

	private WorkGroup workGroup ; 
	
	private Integer relFlag;           //是否包含用户

	public WorkGroupVO() {
		super();
	}

	public WorkGroupVO(WorkGroup workGroup, Integer relFlag) {
		super();
		this.workGroup = workGroup;
		this.relFlag = relFlag;
	}

	public WorkGroup getWorkGroup() {
		return workGroup;
	}

	public void setWorkGroup(WorkGroup workGroup) {
		this.workGroup = workGroup;
	}

	public Integer getRelFlag() {
		return relFlag;
	}

	public void setRelFlag(Integer relFlag) {
		this.relFlag = relFlag;
	}

}
