package com.cyw.mammoth.vo;

import org.apache.commons.lang.builder.ToStringBuilder;

import com.cyw.mammoth.bean.Hbuilding;
/**
 * 建筑VO
 * @author Administrator
 *
 */
public class HbuildingVo {

	/**
	 * 建筑
	 */
	private Hbuilding hbuilding ;
	/**
	 * 该建筑下有效房型数量
	 */
	private Integer roomTypeNum ;
	/**
	 * 该建筑下相关房型数量（包含无效）
	 */
	private Integer referCount ;
	
	
	
	public Hbuilding getHbuilding() {
		return hbuilding;
	}
	public void setHbuilding(Hbuilding hbuilding) {
		this.hbuilding = hbuilding;
	}
	public Integer getRoomTypeNum() {
		return roomTypeNum;
	}
	public void setRoomTypeNum(Integer roomTypeNum) {
		this.roomTypeNum = roomTypeNum;
	}
	
	public Integer getReferCount() {
		return referCount;
	}
	public void setReferCount(Integer referCount) {
		this.referCount = referCount;
	}
	@Override
	public String toString() {
		return ToStringBuilder.reflectionToString(this) ;
	}
	
}
