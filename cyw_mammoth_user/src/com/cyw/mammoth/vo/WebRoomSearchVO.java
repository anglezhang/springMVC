package com.cyw.mammoth.vo;

public class WebRoomSearchVO {

	private String buildId;
	private String floorNo;
	private String roomCharacter;
	private String roomType;
	
	
	public String getBuildId() {
		return buildId;
	}
	public void setBuildId(String buildId) {
		this.buildId = buildId;
	}
	public String getFloorNo() {
		return floorNo;
	}
	public void setFloorNo(String floorNo) {
		this.floorNo = floorNo;
	}
	public String getRoomCharacter() {
		return roomCharacter;
	}
	public void setRoomCharacter(String roomCharacter) {
		this.roomCharacter = roomCharacter;
	}
	public String getRoomType() {
		return roomType;
	}
	public void setRoomType(String roomType) {
		this.roomType = roomType;
	}
	
}
