package com.cyw.mammoth.vo;

import org.apache.commons.lang.builder.ToStringBuilder;

import com.cyw.mammoth.bean.HroomPlanList;
/**
 * 房价方案列表VO
 * @author Administrator
 *
 */
public class HroomPlanListVO {
	
	private HroomPlanList hroomPlanList ;     // 房价方案列表
	private String roomTypeCode ;             // 房类代码
	private String roomTypeName ;             // 房类名称
	private String holidayCode ;              // 节假日代码
	private String holidayName ;              // 节假日名称
	private String flag ;                     // 标识 a 新增  u 修改 d 删除
	
	public HroomPlanListVO() {
		super();
	}
	public HroomPlanList getHroomPlanList() {
		return hroomPlanList;
	}
	public void setHroomPlanList(HroomPlanList hroomPlanList) {
		this.hroomPlanList = hroomPlanList;
	}
	public String getRoomTypeCode() {
		return roomTypeCode;
	}
	public void setRoomTypeCode(String roomTypeCode) {
		this.roomTypeCode = roomTypeCode;
	}
	public String getRoomTypeName() {
		return roomTypeName;
	}
	public void setRoomTypeName(String roomTypeName) {
		this.roomTypeName = roomTypeName;
	}
	public String getHolidayCode() {
		return holidayCode;
	}
	public void setHolidayCode(String holidayCode) {
		this.holidayCode = holidayCode;
	}
	public String getHolidayName() {
		return holidayName;
	}
	public void setHolidayName(String holidayName) {
		this.holidayName = holidayName;
	}
	
	public String getFlag() {
		return flag;
	}
	public void setFlag(String flag) {
		this.flag = flag;
	}
	@Override
	public String toString() {
		return ToStringBuilder.reflectionToString(this);
	}

}
